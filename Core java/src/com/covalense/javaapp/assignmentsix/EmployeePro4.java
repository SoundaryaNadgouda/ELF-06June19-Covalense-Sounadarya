package com.covalense.javaapp.assignmentsix;

public class EmployeePro4 {

	private int id;
	private String name;
	private int sal;

	EmployeePro4(int id, String name, int sal) {
		this.id = id;
		this.name = name;
		this.sal = sal;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setSal(int sal) {
		this.sal = sal;
	}

	public int getSal() {
		return sal;
	}

}
